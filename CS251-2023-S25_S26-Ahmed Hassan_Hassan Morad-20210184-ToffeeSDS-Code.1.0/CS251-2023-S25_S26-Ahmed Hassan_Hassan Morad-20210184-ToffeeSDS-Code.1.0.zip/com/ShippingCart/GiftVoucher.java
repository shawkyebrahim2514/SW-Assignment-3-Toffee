package com.ShippingCart;

public class GiftVoucher {
    private String id;
    private double price;

    /**
     * get the id of the gift voucher
     * @return the id of the gift voucher
     */
    public String getId() {
        return id;
    }
    /**
     * set the id of the gift voucher
     * @param id the id of the gift voucher
     */
    public void setId(String id) {
        this.id = id;
    }
    /**
     * get the price of the gift voucher
     * @return the price of the gift voucher
     */
    public double getPrice() {
        return price;
    }
    /**
     * set the price of the gift voucher
     * @param price the price of the gift voucher
     */
    public void setPrice(double price) {
        this.price = price;
    }

    
}
